# COMPARISION OPERATOR

print( 3 > 2)

print(3 < 2 )

print(3 >= 2)

print(3 == 2)   # equality check

print(3 != 2)   # not equal !=

# ATLEAST ONE IS TRUE MEANS TRUE ELSE FALSE

print(2 > 3 or 2 > 1)

# BOTH MUST BE TRUE THEN ONLY OUTPUT IS TRUE ELSE FALSE


print(2 > 3 and 2 > 1)

# NOT OPERATOR

print(2 > 3 )

print(not 2 > 3)